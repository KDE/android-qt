#include <sys/types.h>
#include <sys/ipc.h>
#include <sys/sem.h>
#include <sys/shm.h>
#include <fcntl.h>

int main(int, char **)
{
    key_t unix_key = ftok("test", 'Q');
    int semaphore = semget(unix_key, 1, 0666 | IPC_CREAT | IPC_EXCL);
    if (semaphore != -1)
        semctl(semaphore, 0, IPC_RMID, 0);

    shmget(unix_key, 0, 0666 | IPC_CREAT | IPC_EXCL);
    shmctl(0, 0, static_cast<struct shmid_ds *>(0));

    return 0;
}
